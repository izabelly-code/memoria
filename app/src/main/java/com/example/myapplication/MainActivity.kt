class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MyApplicationTheme {
                val navController = rememberNavController()

                NavHost(navController = navController, startDestination = "WordView") {
                    composable("WordView") { WordView(navController)  }
                    composable("CardDetailView/{word}/{translation}") { backStackEntry ->
                        val word = backStackEntry.arguments?.getString("word") ?: ""
                        val translation = backStackEntry.arguments?.getString("translation") ?: ""

                        CardDetailView(word = Word(word = word, translation = translation))
                    }
                    composable("ShuffleView") { ShuffleView()  }

                }

            }
        }
    }
}




